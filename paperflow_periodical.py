def write_periodical_project(
    *,
    periodical_dir: str,
    title: str,
    series: str,
    issue: int | None,
    manifest: dict[str, Any],
    manifest_path: str,
    manifest_sha256: str,
    sections: list[dict[str, Any]],
    focus_section: dict[str, Any] | None,
    supporting_sections: list[dict[str, Any]],
    category_summaries: dict[str, dict[str, Any]],
    foundational_sections: list[dict[str, Any]],
    ref_map: dict[str, list[dict[str, Any]]],
    model: str,
    variant: str | None,
    prompt_version: int,
    reference_depth: int,
) -> dict[str, Any]:
    periodical_path = Path(periodical_dir)
    chapters_dir = periodical_path / "chapters"
    periodical_path.mkdir(parents=True, exist_ok=True)
    _clear_generated_tex(chapters_dir)

    _generated_tex(
        chapters_dir / "executive-summary.tex",
        render_executive_summary_chapter(
            sections,
            category_summaries,
            manifest,
            series=series,
            issue=issue,
            focus_section=focus_section,
            supporting_sections=supporting_sections,
        ),
    )
    _generated_tex(
        chapters_dir / "bibliography.tex",
        render_bibliography_chapter(sections, foundational_sections),
    )

    chapter_includes = ["executive-summary"]
    if focus_section:
        focus_filename = _paper_chapter_filename(focus_section["entry"])
        _generated_tex(
            chapters_dir / focus_filename,
            render_paper_chapter(
                focus_section["entry"],
                focus_section["summary"],
                ref_map.get(_entry_id(focus_section["entry"]), []),
            ),
        )
        _generated_tex(
            chapters_dir / "focal-paper.tex",
            "\n".join(
                [
                    r"\section{Focal Paper}",
                    rf"\input{{chapters/{focus_filename.replace('.tex', '')}}}",
                    "",
                ]
            ),
        )
        chapter_includes.append("focal-paper")

    if supporting_sections:
        support_lines = [r"\section{Supporting Context}", ""]
        for item in supporting_sections:
            filename = _paper_chapter_filename(item["entry"])
            _generated_tex(
                chapters_dir / filename,
                render_paper_chapter(
                    item["entry"],
                    item["summary"],
                    ref_map.get(_entry_id(item["entry"]), []),
                ),
            )
            support_lines.append(rf"\input{{chapters/{filename.replace('.tex', '')}}}")
        _generated_tex(
            chapters_dir / "supporting-context.tex",
            "\n".join(support_lines) + "\n",
        )
        chapter_includes.append("supporting-context")

    context_ids = {_entry_id(item["entry"]) for item in [*(supporting_sections or [])]}
    if focus_section:
        context_ids.add(_entry_id(focus_section["entry"]))
    grouped_sections: dict[str, list[dict[str, Any]]] = {}
    for item in sections:
        if _entry_id(item["entry"]) in context_ids:
            continue
        cat = str(item["entry"].get("category") or "Uncategorized")
        grouped_sections.setdefault(cat, []).append(item)

    for category, items in sorted(grouped_sections.items()):
        category_slug = slugify(category)
        chapter_includes.append(category_slug)
        cat_lines = [rf"\section{{{latex_escape(category)}}}", ""]
        for item in items:
            entry = item["entry"]
            filename = _paper_chapter_filename(entry)
            arxiv_id = str(entry.get("resolved_id") or entry.get("arxiv_id", ""))
            paper_refs = ref_map.get(arxiv_id, [])
            _generated_tex(
                chapters_dir / filename,
                render_paper_chapter(entry, item["summary"], paper_refs),
            )
            cat_lines.append(rf"\input{{chapters/{filename.replace('.tex', '')}}}")
        _generated_tex(chapters_dir / f"{category_slug}.tex", "\n".join(cat_lines) + "\n")

    if foundational_sections:
        chapter_includes.append("foundational-references")
        _generated_tex(
            chapters_dir / "foundational-references.tex",
            render_foundational_chapter(foundational_sections),
        )

    main_tex_path = periodical_path / "research-radar.tex"
    main_tex_path.write_text(
        render_periodical_main_tex(
            title=title,
            series=series,
            issue=issue,
            focus_title=str(focus_section["entry"].get("title", "")) if focus_section else None,
            chapter_includes=chapter_includes,
        ),
        encoding="utf-8",
    )

    metadata = {
        "generated_at": datetime.now(UTC).isoformat(),
        "manifest_path": manifest_path,
        "manifest_sha256": manifest_sha256,
        "series": series,
        "issue": issue,
        "focus": _entry_id(focus_section["entry"]) if focus_section else None,
        "focus_title": str(focus_section["entry"].get("title", "")) if focus_section else None,
        "supporting_paper_count": len(supporting_sections),
        "supporting_papers": [_entry_id(item["entry"]) for item in supporting_sections],
        "paper_count": len(sections),
        "foundational_count": len(foundational_sections),
        "chapter_count": len(chapter_includes),
        "model": model,
        "variant": variant,
        "prompt_version": prompt_version,
        "reference_depth": reference_depth,
        "tex_class": "research-radar",
        "main_tex": str(main_tex_path),
    }
    (periodical_path / "build-metadata.json").write_text(
        json.dumps(metadata, indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )
    return metadata


async def build_periodical(
    manifest_path: str,
    *,
    cache_dir: str = DEFAULT_PDF_CACHE_DIR,
    model: str,
    variant: str | None,
    summary_cache_dir: str,
    markdown_cache_dir: str,
    prompt_version: int,
    title: str,
    max_papers: int | None,
    periodical_dir: str = "tex/research-radar",
    series: str = "Research Radar",
    issue: int | None = None,
    focus: str | None = None,
    supporting_papers: int = 6,
    supporting_focuses: list[str] | None = None,
    refresh_summaries: bool = False,
    reference_depth: int = 2,
    max_references_per_paper: int = 10,
    min_reference_citations: int = 50,
    db_path: str | None = None,
) -> dict[str, Any]:
    if max_papers is not None and max_papers < 0:
        raise ValueError("max_papers must be non-negative")
    if reference_depth < 0:
        raise ValueError("reference_depth must be non-negative")
    if max_references_per_paper < 0:
        raise ValueError("max_references_per_paper must be non-negative")
    if supporting_papers < 0:
        raise ValueError("supporting_papers must be non-negative")

    build_id = None
    if db_path:
        from radar_db import get_db, init_db, start_periodical_build

        init_db(db_path)

    await prime_cache(manifest_path, cache_dir)
    manifest = load_manifest(manifest_path)
    entries = [entry for entry in manifest.get("entries", []) if isinstance(entry, dict)]
    if max_papers is not None:
        entries = entries[:max_papers]
    focus_entry = select_focus_entry(entries, focus)
    if supporting_focuses:
        supporting_entries = select_entries_by_ids(entries, supporting_focuses)
    else:
        supporting_entries = select_supporting_entries(entries, focus_entry, supporting_papers)
    issue_entries: list[dict[str, Any]] = []
    seen_issue_ids: set[str] = set()
    for entry in [focus_entry, *supporting_entries]:
        if entry is None:
            continue
        entry_id = _entry_id(entry)
        if entry_id in seen_issue_ids:
            continue
        issue_entries.append(entry)
        seen_issue_ids.add(entry_id)

    manifest_sha256 = hashlib.sha256(Path(manifest_path).read_bytes()).hexdigest()

    if db_path:
        from radar_db import entry_to_paper, record_periodical_paper, upsert_paper

        with get_db(db_path) as conn:
            build_id = start_periodical_build(
                conn,
                paper_count=len(issue_entries),
                reference_depth=reference_depth,
                model=model,
                manifest_sha256=manifest_sha256,
            )
            for entry in issue_entries:
                try:
                    upsert_paper(conn, entry_to_paper(entry, discovered_via="periodical"))
                except Exception:
                    pass
                record_periodical_paper(conn, build_id, str(entry.get("arxiv_id", "")), "primary")

    sections = []
    for entry in issue_entries:
        summary = await summarize_paper_entry(
            entry,
            cache_dir=cache_dir,
            summary_cache_dir=summary_cache_dir,
            markdown_cache_dir=markdown_cache_dir,
            model=model,
            variant=variant,
            prompt_version=prompt_version,
            refresh=refresh_summaries,
            db_path=db_path,
        )
        sections.append({"entry": entry, "summary": summary})
        if db_path:
            _record_enrichment_to_db(
                db_path,
                entry,
                summary,
                model,
                variant,
                prompt_version,
                "llm_summary",
            )

    grouped: dict[str, list[dict[str, Any]]] = {}
    for item in sections:
        grouped.setdefault(str(item["entry"].get("category") or "Uncategorized"), []).append(item)
    category_summaries = {}
    for category, items in grouped.items():
        category_summaries[category] = await summarize_category_section(
            category,
            items,
            summary_cache_dir=summary_cache_dir,
            model=model,
            variant=variant,
            prompt_version=prompt_version,
            refresh=refresh_summaries,
        )
    sections_by_id = {_entry_id(item["entry"]): item for item in sections}
    focus_section = (
        sections_by_id.get(_entry_id(focus_entry)) if focus_entry is not None else None
    )
    supporting_sections = [
        sections_by_id[_entry_id(entry)]
        for entry in supporting_entries
        if _entry_id(entry) in sections_by_id
    ]

    ref_map: dict[str, list[dict[str, Any]]] = {}
    foundational_sections: list[dict[str, Any]] = []
    if reference_depth > 0:
        ref_map = await asyncio.to_thread(
            traverse_references,
            issue_entries,
            max_depth=reference_depth,
            max_refs_per_paper=max_references_per_paper,
            min_citations=min_reference_citations,
        )
        unique_foundational: dict[str, dict[str, Any]] = {}
        for refs in ref_map.values():
            for ref in refs:
                key = ref.get("openalex_id") or ref.get("arxiv_id")
                if key and key not in unique_foundational:
                    unique_foundational[key] = ref
        if db_path:
            from radar_db import entry_to_paper, get_db, record_reference_edges, upsert_paper

            with get_db(db_path) as conn:
                for fref in unique_foundational.values():
                    if fref.get("arxiv_id") and fref["arxiv_id"].startswith(("1", "2")):
                        try:
                            upsert_paper(
                                conn,
                                entry_to_paper(fref, discovered_via="reference_traversal"),
                            )
                        except Exception:
                            pass
                for src, refs in ref_map.items():
                    try:
                        record_reference_edges(conn, normalize_arxiv_id(src), refs)
                    except Exception:
                        pass
        for fentry in sorted(
            unique_foundational.values(),
            key=lambda e: int(e.get("citation_count") or 0),
            reverse=True,
        ):
            if fentry.get("has_pdf") and fentry.get("arxiv_id"):
                try:
                    await _ensure_pdf_cached(fentry, cache_dir)
                    await extract_markdown_from_pdf(
                        fentry,
                        cache_dir=cache_dir,
                        markdown_cache_dir=markdown_cache_dir,
                        db_path=db_path,
                    )
                    fentry["local_pdf_path"] = str(_resolve_pdf_path(fentry, cache_dir))
                except Exception:
                    pass
            try:
                fsummary = await summarize_paper_entry(
                    fentry,
                    cache_dir=cache_dir,
                    summary_cache_dir=summary_cache_dir,
                    markdown_cache_dir=markdown_cache_dir,
                    model=model,
                    variant=variant,
                    prompt_version=prompt_version,
                    refresh=refresh_summaries,
                    db_path=db_path,
                    prompt_kind="foundational",
                )
                foundational_sections.append({"entry": fentry, "summary": fsummary})
                if db_path:
                    _record_enrichment_to_db(
                        db_path,
                        fentry,
                        fsummary,
                        model,
                        variant,
                        prompt_version,
                        "foundational_summary",
                    )
            except Exception:
                foundational_sections.append(
                    {
                        "entry": fentry,
                        "summary": {
                            "results_summary": "Summary unavailable.",
                            "area_of_progress": "",
                            "confidence": "low",
                            "confidence_rationale": "LLM summarization failed.",
                            "primary_claims": [],
                            "required_background": [],
                            "background_research": [],
                            "open_questions": [],
                        },
                    }
                )

    metadata = write_periodical_project(
        periodical_dir=periodical_dir,
        title=title,
        series=series,
        issue=issue,
        manifest=manifest,
        manifest_path=manifest_path,
        manifest_sha256=manifest_sha256,
        sections=sections,
        focus_section=focus_section,
        supporting_sections=supporting_sections,
        category_summaries=category_summaries,
        foundational_sections=foundational_sections,
        ref_map=ref_map,
        model=model,
        variant=variant,
        prompt_version=prompt_version,
        reference_depth=reference_depth,
    )

    periodical_path = Path(periodical_dir)
    dist_dir = periodical_path / ".." / "dist"
    dist_dir.mkdir(parents=True, exist_ok=True)

    build_result = await asyncio.to_thread(
        subprocess.run,
        ["just", "build"],
        cwd=str(periodical_path),
        capture_output=True,
        text=True,
    )
    if build_result.returncode != 0:
        raise RuntimeError(build_result.stderr or build_result.stdout)

    output_pdf = str(dist_dir.resolve() / "research-radar.pdf")
    if db_path and build_id:
        from radar_db import (
            entry_to_paper,
            finish_periodical_build,
            get_db,
            record_periodical_paper,
            upsert_paper,
        )

        with get_db(db_path) as conn:
            for fs in foundational_sections:
                fa_id = str(fs["entry"].get("arxiv_id", ""))
                if fa_id:
                    try:
                        upsert_paper(
                            conn,
                            entry_to_paper(fs["entry"], discovered_via="reference_traversal"),
                        )
                    except Exception:
                        pass
                    record_periodical_paper(conn, build_id, fa_id, "foundational")
            finish_periodical_build(
                conn,
                build_id,
                paper_count=len(sections),
                foundational_count=len(foundational_sections),
                output_pdf=output_pdf,
            )
    return {
        "manifest_path": manifest_path,
        "periodical_dir": str(periodical_path),
        "output_pdf": output_pdf,
        "paper_count": len(sections),
        "foundational_count": len(foundational_sections),
        "model": model,
        "variant": variant,
        "chapter_count": metadata["chapter_count"],
        "series": series,
        "issue": issue,
        "focus": metadata["focus"],
        "summary_index": str(summary_index_path(summary_cache_dir)),
        "build_metadata": str(periodical_path / "build-metadata.json"),
    }
