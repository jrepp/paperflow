GENERATED_HEADER = "%% Auto-generated by arxiv-radar periodical"
DEFAULT_PERIODICAL_QUEUE_PATH = "artifacts/periodical-queue.json"


def _entry_id(entry: dict[str, Any]) -> str:
    return str(entry.get("resolved_id") or entry.get("arxiv_id") or "")


def _published_key(entry: dict[str, Any]) -> str:
    return str(entry.get("published") or entry.get("updated") or "")


def select_focus_entry(
    entries: list[dict[str, Any]],
    focus: str | None = None,
) -> dict[str, Any] | None:
    if not entries:
        return None
    if focus:
        needle = focus.lower()
        for entry in entries:
            ids = {
                str(entry.get("arxiv_id") or "").lower(),
                str(entry.get("resolved_id") or "").lower(),
            }
            title = str(entry.get("title") or "").lower()
            if any(needle in item_id for item_id in ids) or needle in title:
                return entry
        raise ValueError(f"focal paper not found in manifest: {focus}")
    return sorted(
        entries,
        key=lambda entry: (
            int(entry.get("citation_count") or 0),
            _published_key(entry),
            str(entry.get("title") or ""),
        ),
        reverse=True,
    )[0]


def select_supporting_entries(
    entries: list[dict[str, Any]],
    focus_entry: dict[str, Any] | None,
    supporting_papers: int,
) -> list[dict[str, Any]]:
    if supporting_papers < 0:
        raise ValueError("supporting_papers must be non-negative")
    focus_id = _entry_id(focus_entry or {})
    candidates = [entry for entry in entries if _entry_id(entry) != focus_id]
    focus_category = str((focus_entry or {}).get("category") or "")
    return sorted(
        candidates,
        key=lambda entry: (
            str(entry.get("category") or "") == focus_category,
            int(entry.get("citation_count") or 0),
            _published_key(entry),
            str(entry.get("title") or ""),
        ),
        reverse=True,
    )[:supporting_papers]


def select_entries_by_ids(
    entries: list[dict[str, Any]],
    ids: list[str],
) -> list[dict[str, Any]]:
    by_id = {_entry_id(entry): entry for entry in entries}
    by_arxiv_id = {str(entry.get("arxiv_id") or ""): entry for entry in entries}
    selected = []
    for item_id in ids:
        entry = by_id.get(item_id) or by_arxiv_id.get(item_id)
        if entry is not None and entry not in selected:
            selected.append(entry)
    return selected


def _topic_title(entry: dict[str, Any]) -> str:
    area = str(entry.get("category") or "Research")
    title = str(entry.get("title") or "Untitled")
    return f"{area}: {title}"


def _paper_ref(entry: dict[str, Any]) -> dict[str, Any]:
    return {
        "id": _entry_id(entry),
        "arxiv_id": str(entry.get("arxiv_id") or ""),
        "title": str(entry.get("title") or "Untitled"),
        "category": str(entry.get("category") or "Uncategorized"),
        "citation_count": int(entry.get("citation_count") or 0),
        "published": str(entry.get("published") or ""),
    }


def propose_periodical_topics(
    manifest_path: str,
    *,
    limit: int = 8,
    supporting_papers: int = 6,
) -> list[dict[str, Any]]:
    manifest = load_manifest(manifest_path)
    entries = [entry for entry in manifest.get("entries", []) if isinstance(entry, dict)]
    ranked = sorted(
        entries,
        key=lambda entry: (
            int(entry.get("citation_count") or 0),
            _published_key(entry),
            str(entry.get("title") or ""),
        ),
        reverse=True,
    )
    topics = []
    for entry in ranked[:limit]:
        support = select_supporting_entries(entries, entry, supporting_papers)
        topic_id = slugify(_entry_id(entry) or str(entry.get("title") or "topic"))
        topics.append(
            {
                "id": topic_id,
                "title": _topic_title(entry),
                "status": "proposed",
                "focus": _entry_id(entry),
                "focus_paper": _paper_ref(entry),
                "supporting_papers": [_paper_ref(item) for item in support],
                "rationale": str(entry.get("summary") or "")[:500],
            }
        )
    return topics


def load_periodical_queue(queue_path: str = DEFAULT_PERIODICAL_QUEUE_PATH) -> dict[str, Any]:
    path = Path(queue_path)
    if not path.exists():
        return {"version": 1, "topics": []}
    payload = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(payload, dict):
        raise ValueError(f"periodical queue must be an object: {queue_path}")
    payload.setdefault("version", 1)
    payload.setdefault("topics", [])
    return payload


def write_periodical_queue(
    queue: dict[str, Any],
    queue_path: str = DEFAULT_PERIODICAL_QUEUE_PATH,
) -> None:
    path = Path(queue_path)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(queue, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def add_periodical_queue_item(
    *,
    manifest_path: str,
    queue_path: str = DEFAULT_PERIODICAL_QUEUE_PATH,
    focus: str,
    title: str | None = None,
    series: str = "Research Radar",
    issue: int | None = None,
    supporting_papers: int = 6,
) -> dict[str, Any]:
    manifest = load_manifest(manifest_path)
    entries = [entry for entry in manifest.get("entries", []) if isinstance(entry, dict)]
    focus_entry = select_focus_entry(entries, focus)
    if focus_entry is None:
        raise ValueError("cannot queue a topic from an empty manifest")
    support = select_supporting_entries(entries, focus_entry, supporting_papers)
    item = {
        "id": slugify(f"{issue or 'next'}-{_entry_id(focus_entry)}"),
        "status": "queued",
        "series": series,
        "issue": issue,
        "title": title or _topic_title(focus_entry),
        "focus": _entry_id(focus_entry),
        "focus_paper": _paper_ref(focus_entry),
        "supporting_papers": [_paper_ref(entry) for entry in support],
        "created_at": datetime.now(UTC).isoformat(),
        "manifest_path": manifest_path,
    }
    queue = load_periodical_queue(queue_path)
    topics = [topic for topic in queue.get("topics", []) if topic.get("id") != item["id"]]
    topics.append(item)
    queue["topics"] = topics
    write_periodical_queue(queue, queue_path)
    return item


def get_periodical_queue_item(
    queue_item: str,
    queue_path: str = DEFAULT_PERIODICAL_QUEUE_PATH,
) -> dict[str, Any]:
    queue = load_periodical_queue(queue_path)
    for item in queue.get("topics", []):
        if item.get("id") == queue_item:
            return item
    raise ValueError(f"periodical queue item not found: {queue_item}")


def _paper_chapter_filename(entry: dict[str, Any]) -> str:
    arxiv_id = slugify(str(entry.get("resolved_id") or entry.get("arxiv_id", "unknown")))
    return f"{arxiv_id}.tex"


def _generated_tex(path: Path, body: str) -> None:
    path.write_text(f"{GENERATED_HEADER}\n{body}", encoding="utf-8")


def _clear_generated_tex(chapters_dir: Path) -> None:
    chapters_dir.mkdir(parents=True, exist_ok=True)
    for path in chapters_dir.glob("*.tex"):
        path.unlink()


def render_paper_chapter(
    entry: dict[str, Any],
    summary: dict[str, Any],
    foundational_refs: list[dict[str, Any]] | None = None,
) -> str:
    title = latex_escape(str(entry.get("title", "Untitled")))
    arxiv_id = str(entry.get("arxiv_id", ""))
    citations = int(entry.get("citation_count") or 0)
    published = str(entry.get("published", ""))[:10]
    authors = ", ".join((entry.get("authors") or [])[:3])
    if len(entry.get("authors") or []) > 3:
        authors += " et al."
    area = latex_escape(str(summary.get("area_of_progress", "")))
    confidence = str(summary.get("confidence", "unknown")).upper()
    results = latex_escape(str(summary.get("results_summary", "")))
    framing = latex_escape(str(summary.get("intake_framing", "")))
    citation_signal = latex_escape(str(summary.get("citation_signal", "")))
    abs_url = str(entry.get("abs_url", ""))

    lines = [
        r"\section*{" + f"{title}" + "}",
        r"\addcontentsline{toc}{section}{" + f"{title}" + "}",
        "",
        r"\papermeta{",
        rf"\textbf{{{latex_escape(authors)}}} \quad | \quad {published} \quad | \quad {citations} citations \quad | \quad arXiv:{latex_escape(arxiv_id)}",
        r"}",
        "",
    ]
    if area:
        lines.append(rf"\areabox{{{area}}}\quad ")
    lines.append(rf"\confidencebadge{{{confidence}}}")
    lines.append("")

    lines.extend(
        [
            r"\papercard{",
            rf"\insightbox{{{results}}}",
            r"\vspace{0.3em}",
            rf"\textbf{{Why it matters:}} {framing}",
            r"}",
            "",
        ]
    )

    if citation_signal:
        lines.extend(
            [
                r"\noindent\textbf{\color{mediumgray}Citation signal:} ",
                rf"{citation_signal}",
                "",
            ]
        )

    claims = summary.get("primary_claims", [])
    if claims:
        lines.append(r"\noindent\textbf{\color{darkgray}Key Claims}")
        lines.append(r"\begin{itemize}")
        for claim in claims[:5]:
            lines.append(rf"\item {latex_escape(str(claim))}")
        lines.extend([r"\end{itemize}", ""])

    bg = summary.get("required_background", [])
    if bg:
        lines.append(r"\noindent\textbf{\color{mediumgray}Background Needed}")
        lines.append(r"\begin{itemize}")
        for item_text in bg[:4]:
            lines.append(rf"\item {latex_escape(str(item_text))}")
        lines.extend([r"\end{itemize}", ""])

    oq = summary.get("open_questions", [])
    if oq:
        lines.append(r"\noindent\textbf{\color{mediumgray}Open Questions}")
        lines.append(r"\begin{itemize}")
        for item_text in oq[:3]:
            lines.append(rf"\item {latex_escape(str(item_text))}")
        lines.extend([r"\end{itemize}", ""])

    if foundational_refs:
        lines.extend(
            [
                r"\noindent\textbf{\color{mediumgray}Foundational References}",
                r"\begin{itemize}",
            ]
        )
        for ref in foundational_refs[:6]:
            ref_title = latex_escape(str(ref.get("title", "Unknown")))
            ref_year = ref.get("publication_year", "")
            ref_cites = int(ref.get("citation_count") or 0)
            lines.append(rf"\item {ref_title} ({ref_year}, {ref_cites} citations)")
        lines.extend([r"\end{itemize}", ""])

    lines.extend(
        [
            r"\noindent{\footnotesize",
            rf"\textcolor{{accentblue}}{{\url{{{abs_url}}}}}",
            r"}",
            r"\medskip",
            "",
        ]
    )
    return "\n".join(lines) + "\n"


def render_executive_summary_chapter(
    sections: list[dict[str, Any]],
    category_summaries: dict[str, dict[str, Any]],
    manifest: dict[str, Any],
    *,
    series: str,
    issue: int | None,
    focus_section: dict[str, Any] | None,
    supporting_sections: list[dict[str, Any]],
) -> str:
    category_counts: dict[str, int] = {}
    for item in sections:
        cat = str(item["entry"].get("category") or "Uncategorized")
        category_counts[cat] = category_counts.get(cat, 0) + 1

    top_cited = sorted(
        sections,
        key=lambda item: int(item["entry"].get("citation_count") or 0),
        reverse=True,
    )[:5]

    generated_at = str(manifest.get("generated_at") or "")
    issue_label = f"Issue {issue}" if issue is not None else "Unnumbered Issue"
    lines = [
        r"\section*{In This Issue}",
        r"\addcontentsline{toc}{section}{In This Issue}",
        "",
        rf"\textbf{{{latex_escape(series)} {latex_escape(issue_label)}}}",
        "",
        rf"This issue builds a durable reading path around {len(sections)} selected papers across {len(category_counts)} categories.",
        "",
    ]
    if generated_at:
        lines.extend([rf"\textit{{Radar generated: {latex_escape(generated_at)}}}", ""])

    if focus_section:
        entry = focus_section["entry"]
        summary = focus_section["summary"]
        lines.extend(
            [
                r"\section*{Through Line}",
                r"\addcontentsline{toc}{section}{Through Line}",
                "",
                rf"The focal paper is \textbf{{{latex_escape(str(entry.get('title', 'Untitled')))}}}.",
                "",
                latex_escape(str(summary.get("intake_framing") or summary.get("results_summary") or "")),
                "",
            ]
        )
        supporting_titles = [
            latex_escape(str(item["entry"].get("title", "Untitled")))
            for item in supporting_sections
        ]
        if supporting_titles:
            lines.extend(
                [
                    r"\noindent\textbf{\color{mediumgray}Supporting Arc}",
                    r"\begin{itemize}",
                ]
            )
            for title in supporting_titles:
                lines.append(rf"\item {title}")
            lines.extend([r"\end{itemize}", ""])

    if top_cited:
        lines.extend(
            [
                r"\noindent\textbf{\color{darkgray}Highest Citation Signal}",
                r"\begin{itemize}",
            ]
        )
        for item in top_cited:
            entry = item["entry"]
            title = latex_escape(str(entry.get("title", "Untitled")))
            cites = int(entry.get("citation_count") or 0)
            lines.append(rf"\item {title} ({cites} citations)")
        lines.extend([r"\end{itemize}", ""])

    for category, cat_summary in sorted(category_summaries.items()):
        lines.extend(
            [
                rf"\section*{{{latex_escape(category)} Overview}}",
                rf"\addcontentsline{{toc}}{{section}}{{{latex_escape(category)} Overview}}",
                "",
                latex_escape(str(cat_summary.get("executive_summary", ""))),
                "",
            ]
        )
        themes = cat_summary.get("shared_themes", [])
        if themes:
            lines.append(r"\noindent\textbf{\color{mediumgray}Themes}")
            lines.append(r"\begin{itemize}")
            for theme in themes[:4]:
                lines.append(rf"\item {latex_escape(str(theme))}")
            lines.extend([r"\end{itemize}", ""])
        priorities = cat_summary.get("intake_priorities", [])
        if priorities:
            lines.append(r"\noindent\textbf{\color{mediumgray}Reading Order}")
            lines.append(r"\begin{itemize}")
            for priority in priorities[:4]:
                lines.append(rf"\item {latex_escape(str(priority))}")
            lines.extend([r"\end{itemize}", ""])

    return "\n".join(lines) + "\n"


def render_bibliography_chapter(
    sections: list[dict[str, Any]],
    foundational_sections: list[dict[str, Any]] | None = None,
) -> str:
    lines = [
        r"\section*{Bibliography}",
        r"\addcontentsline{toc}{section}{Bibliography}",
        "",
    ]
    grouped: dict[str, list[dict[str, Any]]] = {}
    for item in sections:
        cat = str(item["entry"].get("category") or "Uncategorized")
        grouped.setdefault(cat, []).append(item)
    for category, items in sorted(grouped.items()):
        lines.extend([rf"\section{{{latex_escape(category)}}}", r"\begin{itemize}"])
        for item in items:
            entry = item["entry"]
            title = latex_escape(str(entry.get("title", "Untitled")))
            arxiv_id = latex_escape(str(entry.get("arxiv_id", "")))
            url = str(entry.get("abs_url", ""))
            lines.append(rf"\item {title}. arXiv {arxiv_id}. \url{{{url}}}")
        lines.extend([r"\end{itemize}", ""])
    if foundational_sections:
        lines.extend([r"\section{Foundational References}", r"\begin{itemize}"])
        for item in foundational_sections:
            entry = item["entry"]
            title = latex_escape(str(entry.get("title", "Untitled")))
            cites = int(entry.get("citation_count") or 0)
            year = entry.get("publication_year", "")
            url = str(entry.get("abs_url", ""))
            lines.append(rf"\item {title} ({year}, {cites} citations). \url{{{url}}}")
        lines.extend([r"\end{itemize}", ""])
    return "\n".join(lines) + "\n"


def render_foundational_chapter(foundational_sections: list[dict[str, Any]]) -> str:
    lines = [
        r"\section*{Foundational References}",
        r"\addcontentsline{toc}{section}{Foundational References}",
        "",
        r"These foundational papers were discovered through citation traversal of the primary radar selections. "
        r"Each paper received a full intake summary to help build foundational knowledge.",
        "",
    ]
    for item in foundational_sections:
        entry = item["entry"]
        summary = item["summary"]
        title = latex_escape(str(entry.get("title", "Untitled")))
        citations = int(entry.get("citation_count") or 0)
        year = entry.get("publication_year", "")
        abs_url = str(entry.get("abs_url", ""))
        results = latex_escape(str(summary.get("results_summary", "")))
        area = latex_escape(str(summary.get("area_of_progress", "")))
        confidence = str(summary.get("confidence", "unknown")).upper()
        lines.extend(
            [
                r"\section*{" + f"{title}" + "}",
                rf"\noindent\textbf{{Year:}} {year}\quad \textbf{{Citations:}} {citations}\quad \textbf{{Confidence:}} [{confidence}]",
                "",
                rf"\textit{{{area}}}" if area else "",
                results,
                "",
            ]
        )
        for key, label in [
            ("primary_claims", "Primary Claims"),
            ("required_background", "Required Background"),
            ("long_range_insights", "Long-Range Insights"),
            ("modern_connections", "Modern Connections"),
        ]:
            values = summary.get(key)
            if values:
                lines.extend([rf"\noindent\textbf{{{label}:}}", r"\begin{itemize}"])
                for value in values:
                    lines.append(rf"\item {latex_escape(str(value))}")
                lines.extend([r"\end{itemize}", ""])
        if abs_url:
            lines.extend([rf"\noindent\url{{{abs_url}}}", ""])
        lines.extend([r"\medskip", ""])
    return "\n".join(lines) + "\n"


